public class SignDemo {
    public static void main(String[] args) {
        //\b - 退格
        System.out.println("OO\bPP");

        //\t - tab键
        System.out.println("Ja\tva");

        //转义字符\
        System.out.println("\\\\");// \\

        System.out.println("james:\"java天下无敌\"");

        System.out.println("D:\\news\\temp");
        System.out.println("D:/news/temp");
    }
}
